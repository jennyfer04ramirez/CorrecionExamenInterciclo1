package ec.edu.ups.ramirezcamila.ejercicioexamen1poo.modelo;

/**
 *
 * @author ESTUDIANTE
 */
public class Producto {
    private int codigo;
    private String nombre;
    private String descrpcion;
    private double precio;

    public Producto() {
    }

    public Producto(int codigo, String nombre, String descrpcion, double precio) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.descrpcion = descrpcion;
        this.precio = precio;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescrpcion() {
        return descrpcion;
    }

    public void setDescrpcion(String descrpcion) {
        this.descrpcion = descrpcion;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 41 * hash + this.codigo;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Producto other = (Producto) obj;
        return this.codigo == other.codigo;
    }

    @Override
    public String toString() {
        return "Producto{" + "codigo=" + codigo + ", nombre=" + nombre + ", descrpcion=" + descrpcion + ", precio=" + precio + '}';
    }   
}